package com.zzxy.NetDict.Entity;

public class File {

	private String id;//fileId
	private int type;//fileType
	private double size;//file size
	private String descrp;//file`s description
	private String thumbpath;//file`s thumb path
	private String transitionPath;//file`s transitionPath
	
	
	public String getId() {
		return id;
	}
	public void setId(String id) {
		this.id = id;
	}
	public int getType() {
		return type;
	}
	public void setType(int type) {
		this.type = type;
	}
	public double getSize() {
		return size;
	}
	public void setSize(double size) {
		this.size = size;
	}
	public String getDescrp() {
		return descrp;
	}
	public void setDescrp(String descrp) {
		this.descrp = descrp;
	}
	public String getThumbpath() {
		return thumbpath;
	}
	public void setThumbpath(String thumbpath) {
		this.thumbpath = thumbpath;
	}
	public String getTransitionPath() {
		return transitionPath;
	}
	public void setTransitionPath(String transitionPath) {
		this.transitionPath = transitionPath;
	}
	
	
	
	
	
	
}
